            <h2>Draft</h2>
            <h3>Rookie Draft</h3>
            <ul>
                <li>Linear, 4-round, slow draft, held approximately after the conclusion of the NFL Draft</li>
                <li>Each manager will have 4 hours to make each selection</li>
                <li>If a manager fails to make a selection within the allotted amount of time, they will receive the best available player</li>
                <li>The draft will automatically pause between 12:00 AM - 8:00 AM</li>
            </ul>
            <p>Picks 1-4 will be assigned based on a hybrid points system which will then be used as a part of a lottery system (combines regular season and postseason performance):</p>
            <ul>
                <li>Regular Season Finish (4 times seed in consolation bracket)
                    <ul>
                        <li>12th Place (4 Seed)- 16 points</li>
                        <li>11th Place (3 Seed)- 12 points</li>
                        <li>10th Place (2 Seed)- 8 points</li>
                        <li>9th Place (1 Seed)- 4 points</li>
                    </ul>
                </li>
                <li>Consolation Bracket Finish
                    <ul>
                        <li>Winner- 8 points</li>
                        <li>Runner Up- 4 points</li>
                        <li>3rd Place- 2 points</li>
                        <li>4th Place- 1 point</li>
                    </ul>
                </li>
                <li>Any ties will be broken based on the worse seed</li>
                <li>Lottery Balls (based on total number of points awarded) using <a href="https://www.fantasydraftlottery.com/">https://www.fantasydraftlottery.com/</a>
                    <ul>
                        <li>1st- 50</li>
                        <li>2nd- 30</li>
                        <li>3rd- 15</li>
                        <li>4th- 5</li>
                    </ul>
                </li>
                <li>Pick 5 will be automatically awarded to the 8th-place team from the regular season</li>
                <li>Pick 6 will be awarded to the worst seed remaining of the eliminated teams from the winner’s bracket</li>
                <li>Picks 7-11 will be based on how the winner’s consolation bracket games pan out (ex. In the 5th place game, the loser will be assigned the better pick)</li>
                <li>Pick 12 assigned to the previous season’s champion</li>
                <li>Both rookies and veterans (free agents) may be drafted</li>
            </ul>

            <h3>Startup Draft</h3>
            <ul>
                <li>The Inaugural Veteran Draft will consist of a 27-round snake draft with NO third-round reversal; includes ONLY veterans</li>
                <li>The draft is scheduled to begin on June 30th, 2023 at noon EDT</li>
                <li>The draft order is filled out with the help of a random order generator. However, the order gives managers the ability to pick where they can draft from (in either the veteran draft or the initial rookie draft)</li>
                <li>Managers will be allotted 8 hours to make each selection</li>
                <li>If a manager does not make a selection within the allotted amount of time, the best player will be automatically selected for them</li>
                <li>The draft will automatically pause between 12:00 AM - 8:00 AM</li>
                <li>In future years, the draft order for the subsequent year will be based on the final standings from the previous year, with the worst team picking first and the champion picking last</li>
            </ul>

            <h2>Trades</h2>
            <ul>
                <li>All trades must be submitted through the Sleeper app/website</li>
                <li>Trade Deadline: TBD</li>
                <li>Draft Pick Trading: Allowed up to 2 years into the future (ex. once the 2023 Veteran Draft is completed, managers can only trade for 2023 and 2024 picks)</li>
                <li>Trading FAAB: Not allowed</li>
            </ul>

            <h2>Waivers/Free Agents</h2>
            <ul>
                <li>Blind bid waivers with a rolling $100 FAAB</li>
                <li>Initial waivers will be run two days after the Veteran Draft is completed</li>
                <li>Waivers will then be run twice a week on Wednesday and Friday</li>
                <li>Free agents may be acquired first-come, first-serve after the Friday waivers run and after each subsequent waivers run (until Sunday 1:00 PM)</li>
            </ul>

            <h2>Taxi Squad</h2>
            <ul>
                <li>4 Taxi Squad Spots (exclusively for players in their first or second year of the league)</li>
                <li>Can be moved to and from the active roster at any point (but are subject to waiver rules)</li>
            </ul>

            <h2>IR</h2>
            <ul>
                <li>There are three IR spots that can only be filled by players who are on IR or PUP in the NFL</li>
                <li>Players can remain on IR once activated, but must be moved to the active roster in order to accumulate points</li>
                <li>Players can also be cut directly from IR</li>
            </ul>

            <h2>Rule Change Process</h2>
